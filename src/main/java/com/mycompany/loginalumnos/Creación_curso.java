public void leerJson(String rutaJson) {
    JSONParser jp = new JSONParser();
    try {
        FileReader r = new FileReader(rutaJson);
        Object o = jp.parse(r);
        JSONArray cursosList = (JSONArray) o;

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd'T'HH:mm:ss.SSSZ");
        System.out.println(cursosList);

        for (Object cursos : cursosList) {
            JSONObject datosCurso = (JSONObject) cursos;

            String id = String.valueOf(datosCurso.get("id"));
            String nombre = (String) datosCurso.get("nombre");
            String seccion = (String) datosCurso.get("seccion");
            String fechaInicioStr = (String) datosCurso.get("fecha_inicio");
            String fechaFinStr = (String) datosCurso.get("fecha_fin");
            String horaInicio = (String) datosCurso.get("hora_inicio");
            String horaFin = (String) datosCurso.get("hora_fin");
            String profesor = (String) datosCurso.get("profesor");

            Date fechaInicio = null;
            Date fechaFin = null;
            fechaInicio = sdf.parse(fechaInicioStr.replaceAll("Z$", "+0000")); // Saltar a la siguiente iteración si hay un error de análisis
            fechaFin = sdf.parse(fechaFinStr.replaceAll("Z$", "+0000"));

            Clase c = new Clase();
            c.setId(id);
            c.setNombre(nombre);
            c.setSeccion(seccion);
            c.setFechaInicio(fechaInicio);
            c.setFechaFin(fechaFin);
            c.setHoraInicio(horaInicio);
            c.setHoraFin(horaFin);
            c.setProfesorasignado(profesor);

            Profesor profesorAsignado = encontrarProfesor(profesor);
            if (profesorAsignado != null) {
                profesorAsignado.getClasesAsignadasProfesor().add(c);
            }

            Loginalumnos.clases.add(c);

        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}

