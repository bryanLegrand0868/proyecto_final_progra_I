    public Profesor encontrarProfesor(String usuario){
        
        Profesor returnProfesor = null;
        
        for (Profesor p : Loginalumnos.usuarios){
            if(p.getUsuario().equals(usuario)){
                returnProfesor = p;
            }
        }
        return returnProfesor;
    }
    

