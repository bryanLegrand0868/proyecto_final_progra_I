    private void saveAlumnosToXML(File file) {
    try {
        DocumentBuilderFactory dbFactory = DocumentBuilderFactory.newInstance();
        DocumentBuilder dBuilder = dbFactory.newDocumentBuilder();
        Document doc = dBuilder.newDocument();

        // Elemento ra√≠z
        Element rootElement = doc.createElement("alumno");
        doc.appendChild(rootElement);

        for (alumno alumno : Loginalumnos.usuarioalumno) {
            // Elemento alumno
            Element alumnoElement = doc.createElement("alumno");
            rootElement.appendChild(alumnoElement);

            // Elemento carnet
            Element carnet = doc.createElement("carnet");
            carnet.appendChild(doc.createTextNode(String.valueOf(alumno.getCarnet())));
            alumnoElement.appendChild(carnet);

            // Elemento nombre
            Element nombre = doc.createElement("nombre");
            nombre.appendChild(doc.createTextNode(alumno.getNombre()));
            alumnoElement.appendChild(nombre);
        }

        // Escribiendo el contenido en un archivo XML
        TransformerFactory transformerFactory = TransformerFactory.newInstance();
        Transformer transformer = transformerFactory.newTransformer();
        DOMSource source = new DOMSource(doc);
        StreamResult result = new StreamResult(file);

        transformer.transform(source, result);

        JOptionPane.showMessageDialog(this, "Informaci√≥n guardada en " + file.getAbsolutePath());

    } catch (ParserConfigurationException | TransformerException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al guardar el archivo XML.");
    }
}
    
    
