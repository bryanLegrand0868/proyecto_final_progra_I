    public void readFileXML(File jf){
        try{
            DocumentBuilderFactory dbf = DocumentBuilderFactory.newInstance();
            DocumentBuilder db = dbf.newDocumentBuilder();
            Document doc = db.parse(jf);
            
            doc.getDocumentElement().normalize();
            NodeList nodos = doc.getElementsByTagName("alumno");
            
            for (int i = 0; i < nodos.getLength(); i++) {
                Node nodo = nodos.item(i);
                if (nodo.getNodeType() == Node.ELEMENT_NODE) {
                    Element e = (Element) nodo;
                    alumno a = new alumno();
                    Node carnetNode = e.getElementsByTagName("carnet").item(0);
                    Node nombreNode = e.getElementsByTagName("nombre").item(0);
                    Node apellidoNode = e.getElementsByTagName("apellido").item(0);
                    Node passwordNode = e.getElementsByTagName("password").item(0);


                    if (carnetNode != null && nombreNode != null) {
                        a.setCarnet(carnetNode.getTextContent());
                        a.setNombre(nombreNode.getTextContent());
                        a.setApellido(apellidoNode.getTextContent());
                        a.setPassword(passwordNode.getTextContent());
                        Loginalumnos.usuarioalumno.add(a);
                    }
                }
                
            }
            
            
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }
