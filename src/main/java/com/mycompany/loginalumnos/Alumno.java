    private boolean containsSpecialCharacter(String str) {
    String specialChars = "!@#$%^&*()_+-=[]{};':\"\\|,.<>?/";
    for (char ch : str.toCharArray()) {
        if (specialChars.contains(Character.toString(ch))) {
            return true;
        }
    }
    return false;
    }
    
